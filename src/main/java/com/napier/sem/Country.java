package com.napier.sem;

/**
 * Represents a Country
 */
public class Country {
    /**
     * Country's code
     */
    public String code;

    /**
     * Country's name
     */
    public String name;

    /**
     * Country's continent
     */
    public String continent;

    /**
     * Country's region
     */
    public String region;

    /**
     * Country's surface area
     */
    public Double surface_area;

    /**
     * Country's Independence Year
     *
     */
    public Short independence_year;

    /**
     * Country's population
     *
     */
    public int population;

    /**
     * Country's life expectancy
     *
     */
    public Double life_expectancy;

    /**
     * Country's Gross National Product
     *
     */
    public Double gnp;

    /**
     * Country's Old Gross National Product
     *
     */
    public Double old_gnp;

    /**
     * Country's local name
     *
     */
    public String local_name;

    /**
     * Country's government type
     *
     */
    public String government_type;

    /**
     * Country's head of state
     *
     */
    public String head_of_state;

    /**
     * Country's capital
     *
     */
    public int capital;

    /**
     * Country's code 2
     *
     */
    public String code_two;



}
